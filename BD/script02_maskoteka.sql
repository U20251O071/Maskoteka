CREATE PROCEDURE dbo.usp_Usuario_Registrar
    @NOMBRES            VARCHAR(80),
    @APELLIDOS          VARCHAR(100),
    @CORREO             VARCHAR(120),
    @CLAVE              VARCHAR(255),
    @TELEFONO           VARCHAR(20) = NULL,
    @TIPO_DOCUMENTO     VARCHAR(10) = NULL,
    @NUMERO_DOCUMENTO   VARCHAR(15) = NULL,
    @DIRECCION          VARCHAR(200) = NULL,
    @ID_USUARIO         INT OUTPUT
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS (SELECT 1 FROM dbo.TM_USUARIO WHERE CORREO = LOWER(LTRIM(RTRIM(@CORREO))))
        THROW 50001, 'El correo ya se encuentra registrado.', 1;

    DECLARE @ID_ROL_CLIENTE INT = (SELECT ID_ROL FROM dbo.TM_ROL WHERE NOMBRE = 'CLIENTE');

    INSERT INTO dbo.TM_USUARIO
        (ID_ROL, NOMBRES, APELLIDOS, TIPO_DOCUMENTO, NUMERO_DOCUMENTO,
         TELEFONO, CORREO, CLAVE, DIRECCION)
    VALUES
        (@ID_ROL_CLIENTE, LTRIM(RTRIM(@NOMBRES)), LTRIM(RTRIM(@APELLIDOS)),
         @TIPO_DOCUMENTO, @NUMERO_DOCUMENTO, @TELEFONO,
         LOWER(LTRIM(RTRIM(@CORREO))), @CLAVE, @DIRECCION);

    SET @ID_USUARIO = CONVERT(INT, SCOPE_IDENTITY());
END;
GO

