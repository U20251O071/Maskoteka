CREATE PROCEDURE dbo.usp_Cita_Cancelar
    @ID_CITA        INT,
    @ID_USUARIO     INT,
    @MOTIVO         VARCHAR(300) = NULL
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        DECLARE @ID_DISPONIBILIDAD INT, @ESTADO_ANTERIOR VARCHAR(15);

        SELECT @ID_DISPONIBILIDAD = C.ID_DISPONIBILIDAD,
               @ESTADO_ANTERIOR = C.ESTADO
        FROM dbo.TB_CITA C
        INNER JOIN dbo.TM_MASCOTA M ON M.ID_MASCOTA = C.ID_MASCOTA
        WHERE C.ID_CITA = @ID_CITA
          AND M.ID_USUARIO = @ID_USUARIO
          AND C.ESTADO IN ('PENDIENTE','CONFIRMADA');

        IF @ID_DISPONIBILIDAD IS NULL
            THROW 50010, 'La cita no existe o no puede cancelarse.', 1;

        UPDATE dbo.TB_CITA
           SET ESTADO = 'CANCELADA',
               FECHA_MODIFICACION = SYSDATETIME()
         WHERE ID_CITA = @ID_CITA;

        UPDATE dbo.TB_DISPONIBILIDAD
           SET ESTADO = 'DISPONIBLE'
         WHERE ID_DISPONIBILIDAD = @ID_DISPONIBILIDAD;

        INSERT INTO dbo.TB_CITA_ESTADO_HISTORIAL
            (ID_CITA, ESTADO_ANTERIOR, ESTADO_NUEVO, MOTIVO, ID_USUARIO_ACCION)
        VALUES
            (@ID_CITA, @ESTADO_ANTERIOR, 'CANCELADA', @MOTIVO, @ID_USUARIO);

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END;
GO

