CREATE PROCEDURE dbo.usp_Mascota_Obtener @ID_MASCOTA INT,@ID_USUARIO INT AS
BEGIN SET NOCOUNT ON;
 SELECT M.ID_MASCOTA,M.ID_USUARIO,M.ID_ESPECIE,M.NOMBRE,E.NOMBRE,M.RAZA,M.SEXO,M.FECHA_NACIMIENTO,M.PESO_KG,M.COLOR,M.ESTERILIZADO,M.MICROCHIP,M.FOTO_URL,M.OBSERVACIONES FROM dbo.TM_MASCOTA M JOIN dbo.TM_ESPECIE E ON E.ID_ESPECIE=M.ID_ESPECIE WHERE M.ID_MASCOTA=@ID_MASCOTA AND M.ID_USUARIO=@ID_USUARIO AND M.ACTIVO=1;
 SELECT VA.ID_VACUNA,VA.NOMBRE_VACUNA,VA.FECHA_APLICACION,VA.FECHA_PROXIMA,CONCAT(V.NOMBRES,' ',V.APELLIDOS),VA.OBSERVACIONES FROM dbo.TB_VACUNA_MASCOTA VA LEFT JOIN dbo.TM_VETERINARIO V ON V.ID_VETERINARIO=VA.ID_VETERINARIO WHERE VA.ID_MASCOTA=@ID_MASCOTA ORDER BY VA.FECHA_APLICACION DESC;
 SELECT CO.ID_CONSULTA,CO.FECHA_CONSULTA,CO.DIAGNOSTICO,CO.TRATAMIENTO,CO.PESO_KG,CONCAT(V.NOMBRES,' ',V.APELLIDOS) FROM dbo.TB_CONSULTA_MASCOTA CO LEFT JOIN dbo.TM_VETERINARIO V ON V.ID_VETERINARIO=CO.ID_VETERINARIO WHERE CO.ID_MASCOTA=@ID_MASCOTA ORDER BY CO.FECHA_CONSULTA DESC;
 SELECT ID_DOCUMENTO,NOMBRE,TIPO,URL,FECHA_CREACION FROM dbo.TB_DOCUMENTO_MASCOTA WHERE ID_MASCOTA=@ID_MASCOTA ORDER BY FECHA_CREACION DESC;
END;
GO
