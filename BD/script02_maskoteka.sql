CREATE PROCEDURE dbo.usp_Pago_Registrar
    @ID_CITA              INT,
    @METODO               VARCHAR(20),
    @ESTADO               VARCHAR(15),
    @CODIGO_TRANSACCION   VARCHAR(80) = NULL,
    @ULTIMOS_4            CHAR(4) = NULL,
    @ID_PAGO              INT OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        DECLARE @MONTO DECIMAL(10,2), @ESTADO_CITA VARCHAR(15);

        SELECT @MONTO = PRECIO_RESERVADO, @ESTADO_CITA = ESTADO
        FROM dbo.TB_CITA WITH (UPDLOCK, HOLDLOCK)
        WHERE ID_CITA = @ID_CITA;

        IF @MONTO IS NULL
            THROW 50011, 'La cita indicada no existe.', 1;

        IF @ESTADO_CITA = 'CANCELADA'
            THROW 50012, 'No se puede pagar una cita cancelada.', 1;

        INSERT INTO dbo.TB_PAGO
            (ID_CITA, MONTO, METODO, ESTADO, CODIGO_TRANSACCION,
             ULTIMOS_4, FECHA_PAGO)
        VALUES
            (@ID_CITA, @MONTO, @METODO, @ESTADO, @CODIGO_TRANSACCION,
             @ULTIMOS_4, CASE WHEN @ESTADO = 'APROBADO' THEN SYSDATETIME() ELSE NULL END);

        SET @ID_PAGO = CONVERT(INT, SCOPE_IDENTITY());

        IF @ESTADO = 'APROBADO' AND @ESTADO_CITA = 'PENDIENTE'
        BEGIN
            UPDATE dbo.TB_CITA
               SET ESTADO = 'CONFIRMADA',
                   FECHA_MODIFICACION = SYSDATETIME()
             WHERE ID_CITA = @ID_CITA;

            INSERT INTO dbo.TB_CITA_ESTADO_HISTORIAL
                (ID_CITA, ESTADO_ANTERIOR, ESTADO_NUEVO, MOTIVO)
            VALUES
                (@ID_CITA, 'PENDIENTE', 'CONFIRMADA', 'Pago aprobado');
        END;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END;
GO

