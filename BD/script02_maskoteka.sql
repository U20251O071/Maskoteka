CREATE PROCEDURE dbo.usp_Mascota_Eliminar
    @ID_MASCOTA INT,
    @ID_USUARIO INT
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS
    (
        SELECT 1 FROM dbo.TB_CITA
        WHERE ID_MASCOTA = @ID_MASCOTA
          AND ESTADO IN ('PENDIENTE','CONFIRMADA')
