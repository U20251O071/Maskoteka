CREATE PROCEDURE dbo.usp_Disponibilidad_Listar
    @FECHA_DESDE       DATE,
    @FECHA_HASTA       DATE,
    @ID_VETERINARIO   INT = NULL
AS
BEGIN
    SET NOCOUNT ON;

    SELECT D.ID_DISPONIBILIDAD, D.FECHA_ATENCION, D.HORA_INICIO, D.HORA_FIN,
           V.ID_VETERINARIO,
           CONCAT(V.NOMBRES, ' ', V.APELLIDOS) AS VETERINARIO,
           V.ESPECIALIDAD
    FROM dbo.TB_DISPONIBILIDAD D
    INNER JOIN dbo.TM_VETERINARIO V ON V.ID_VETERINARIO = D.ID_VETERINARIO
    WHERE D.FECHA_ATENCION BETWEEN @FECHA_DESDE AND @FECHA_HASTA
      AND D.ESTADO = 'DISPONIBLE'
      AND V.ACTIVO = 1
      AND (@ID_VETERINARIO IS NULL OR D.ID_VETERINARIO = @ID_VETERINARIO)
    ORDER BY D.FECHA_ATENCION, D.HORA_INICIO;
END;
GO

