CREATE PROCEDURE dbo.usp_Usuario_CambiarClave
    @ID_USUARIO INT,
    @CLAVE_NUEVA VARCHAR(255)
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    IF @ID_USUARIO IS NULL OR @ID_USUARIO <= 0
        THROW 50109, 'El identificador del usuario no es válido.', 1;

    IF NULLIF(LTRIM(RTRIM(@CLAVE_NUEVA)), '') IS NULL
        THROW 50110, 'La nueva contraseña es obligatoria.', 1;

    IF NOT EXISTS
    (
        SELECT 1
        FROM dbo.TM_USUARIO
        WHERE ID_USUARIO = @ID_USUARIO
          AND ACTIVO = 1
    )
        THROW 50111, 'Usuario no encontrado o inactivo.', 1;

    UPDATE dbo.TM_USUARIO
       SET CLAVE = @CLAVE_NUEVA,
           FECHA_MODIFICACION = SYSDATETIME()
     WHERE ID_USUARIO = @ID_USUARIO
       AND ACTIVO = 1;
END;
GO

