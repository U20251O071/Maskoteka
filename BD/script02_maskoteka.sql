CREATE PROCEDURE dbo.usp_Cita_Registrar
    @ID_USUARIO         INT,
    @ID_MASCOTA         INT,
    @ID_SERVICIO        INT,
    @ID_DISPONIBILIDAD  INT,
    @MOTIVO_CONSULTA    VARCHAR(500) = NULL,
    @ID_CITA            INT OUTPUT
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        IF NOT EXISTS
        (
            SELECT 1 FROM dbo.TM_MASCOTA
            WHERE ID_MASCOTA = @ID_MASCOTA
              AND ID_USUARIO = @ID_USUARIO
              AND ACTIVO = 1
        )
            THROW 50005, 'La mascota no pertenece al usuario indicado.', 1;

        DECLARE @ID_VETERINARIO INT;

        SELECT @ID_VETERINARIO = ID_VETERINARIO
        FROM dbo.TB_DISPONIBILIDAD WITH (UPDLOCK, HOLDLOCK)
        WHERE ID_DISPONIBILIDAD = @ID_DISPONIBILIDAD
          AND ESTADO = 'DISPONIBLE';

        IF @ID_VETERINARIO IS NULL
            THROW 50006, 'El horario ya no se encuentra disponible.', 1;

        DECLARE @PRECIO DECIMAL(10,2) =
        (
            SELECT PRECIO FROM dbo.TM_SERVICIO
            WHERE ID_SERVICIO = @ID_SERVICIO AND ACTIVO = 1
        );

        IF @PRECIO IS NULL
            THROW 50007, 'El servicio no existe o está inactivo.', 1;

        INSERT INTO dbo.TB_CITA
            (ID_MASCOTA, ID_SERVICIO, ID_VETERINARIO, ID_DISPONIBILIDAD,
             MOTIVO_CONSULTA, ESTADO, PRECIO_RESERVADO)
        VALUES
            (@ID_MASCOTA, @ID_SERVICIO, @ID_VETERINARIO, @ID_DISPONIBILIDAD,
             @MOTIVO_CONSULTA, 'PENDIENTE', @PRECIO);

        SET @ID_CITA = CONVERT(INT, SCOPE_IDENTITY());

        UPDATE dbo.TB_DISPONIBILIDAD
           SET ESTADO = 'RESERVADO'
         WHERE ID_DISPONIBILIDAD = @ID_DISPONIBILIDAD;

        INSERT INTO dbo.TB_CITA_ESTADO_HISTORIAL
            (ID_CITA, ESTADO_ANTERIOR, ESTADO_NUEVO, MOTIVO, ID_USUARIO_ACCION)
        VALUES
            (@ID_CITA, NULL, 'PENDIENTE', 'Registro inicial de la cita', @ID_USUARIO);

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END;
GO

