CREATE PROCEDURE dbo.usp_Cita_ListarPorUsuario
    @ID_USUARIO INT,
    @ESTADO     VARCHAR(15) = NULL
AS
BEGIN
    SET NOCOUNT ON;

    SELECT C.ID_CITA, M.ID_MASCOTA, M.NOMBRE AS MASCOTA,
           S.NOMBRE AS SERVICIO, C.PRECIO_RESERVADO,
           D.FECHA_ATENCION, D.HORA_INICIO, D.HORA_FIN,
           CONCAT(V.NOMBRES, ' ', V.APELLIDOS) AS VETERINARIO,
           C.ESTADO, C.MOTIVO_CONSULTA, C.FECHA_CREACION
    FROM dbo.TB_CITA C
    INNER JOIN dbo.TM_MASCOTA M ON M.ID_MASCOTA = C.ID_MASCOTA
    INNER JOIN dbo.TM_SERVICIO S ON S.ID_SERVICIO = C.ID_SERVICIO
    INNER JOIN dbo.TB_DISPONIBILIDAD D ON D.ID_DISPONIBILIDAD = C.ID_DISPONIBILIDAD
    INNER JOIN dbo.TM_VETERINARIO V ON V.ID_VETERINARIO = C.ID_VETERINARIO
    WHERE M.ID_USUARIO = @ID_USUARIO
      AND (@ESTADO IS NULL OR C.ESTADO = @ESTADO)
    ORDER BY D.FECHA_ATENCION DESC, D.HORA_INICIO DESC;
END;
GO

