        THROW 50004, 'No se puede eliminar una mascota con citas pendientes o confirmadas.', 1;

    UPDATE dbo.TM_MASCOTA
       SET ACTIVO = 0,
           FECHA_MODIFICACION = SYSDATETIME()
     WHERE ID_MASCOTA = @ID_MASCOTA
       AND ID_USUARIO = @ID_USUARIO;
END;
