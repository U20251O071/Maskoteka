 (SELECT COUNT(*) FROM dbo.TB_CITA C JOIN dbo.TM_MASCOTA M ON M.ID_MASCOTA=C.ID_MASCOTA JOIN dbo.TB_DISPONIBILIDAD D ON D.ID_DISPONIBILIDAD=C.ID_DISPONIBILIDAD WHERE M.ID_USUARIO=@ID_USUARIO AND C.ESTADO IN('PENDIENTE','CONFIRMADA') AND D.FECHA_ATENCION>=CAST(GETDATE() AS DATE)) CITAS_PROXIMAS,
