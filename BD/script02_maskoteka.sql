CREATE PROCEDURE dbo.usp_Cita_Reprogramar
    @ID_CITA                    INT,
    @ID_USUARIO                 INT,
    @ID_NUEVA_DISPONIBILIDAD    INT
AS
BEGIN
    SET NOCOUNT ON;
    SET XACT_ABORT ON;

    BEGIN TRY
        BEGIN TRANSACTION;

        DECLARE @ID_DISP_ANTERIOR INT;

        SELECT @ID_DISP_ANTERIOR = C.ID_DISPONIBILIDAD
        FROM dbo.TB_CITA C
        INNER JOIN dbo.TM_MASCOTA M ON M.ID_MASCOTA = C.ID_MASCOTA
        WHERE C.ID_CITA = @ID_CITA
          AND M.ID_USUARIO = @ID_USUARIO
          AND C.ESTADO IN ('PENDIENTE','CONFIRMADA');

        IF @ID_DISP_ANTERIOR IS NULL
            THROW 50008, 'La cita no existe, no pertenece al usuario o no puede reprogramarse.', 1;

        DECLARE @ID_NUEVO_VETERINARIO INT;

        SELECT @ID_NUEVO_VETERINARIO = ID_VETERINARIO
        FROM dbo.TB_DISPONIBILIDAD WITH (UPDLOCK, HOLDLOCK)
        WHERE ID_DISPONIBILIDAD = @ID_NUEVA_DISPONIBILIDAD
          AND ESTADO = 'DISPONIBLE';

        IF @ID_NUEVO_VETERINARIO IS NULL
            THROW 50009, 'El nuevo horario ya no está disponible.', 1;

        UPDATE dbo.TB_DISPONIBILIDAD SET ESTADO = 'DISPONIBLE'
        WHERE ID_DISPONIBILIDAD = @ID_DISP_ANTERIOR;

        UPDATE dbo.TB_DISPONIBILIDAD SET ESTADO = 'RESERVADO'
        WHERE ID_DISPONIBILIDAD = @ID_NUEVA_DISPONIBILIDAD;

        UPDATE dbo.TB_CITA
           SET ID_DISPONIBILIDAD = @ID_NUEVA_DISPONIBILIDAD,
               ID_VETERINARIO = @ID_NUEVO_VETERINARIO,
               FECHA_MODIFICACION = SYSDATETIME()
         WHERE ID_CITA = @ID_CITA;

        INSERT INTO dbo.TB_CITA_ESTADO_HISTORIAL
            (ID_CITA, ESTADO_ANTERIOR, ESTADO_NUEVO, MOTIVO, ID_USUARIO_ACCION)
        SELECT ID_CITA, ESTADO, ESTADO, 'Cita reprogramada', @ID_USUARIO
        FROM dbo.TB_CITA WHERE ID_CITA = @ID_CITA;

        COMMIT TRANSACTION;
    END TRY
    BEGIN CATCH
        IF @@TRANCOUNT > 0 ROLLBACK TRANSACTION;
        THROW;
    END CATCH
END;
GO

