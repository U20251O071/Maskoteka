CREATE PROCEDURE dbo.usp_Cita_Obtener @ID_CITA INT,@ID_USUARIO INT AS
BEGIN SET NOCOUNT ON;
 SELECT C.ID_CITA,M.ID_MASCOTA,M.NOMBRE,E.NOMBRE,M.RAZA,S.ID_SERVICIO,S.NOMBRE,C.PRECIO_RESERVADO,D.ID_DISPONIBILIDAD,D.FECHA_ATENCION,D.HORA_INICIO,D.HORA_FIN,V.ID_VETERINARIO,CONCAT(V.NOMBRES,' ',V.APELLIDOS),V.ESPECIALIDAD,C.ESTADO,C.MOTIVO_CONSULTA,C.OBSERVACIONES FROM dbo.TB_CITA C JOIN dbo.TM_MASCOTA M ON M.ID_MASCOTA=C.ID_MASCOTA JOIN dbo.TM_ESPECIE E ON E.ID_ESPECIE=M.ID_ESPECIE JOIN dbo.TM_SERVICIO S ON S.ID_SERVICIO=C.ID_SERVICIO JOIN dbo.TB_DISPONIBILIDAD D ON D.ID_DISPONIBILIDAD=C.ID_DISPONIBILIDAD JOIN dbo.TM_VETERINARIO V ON V.ID_VETERINARIO=C.ID_VETERINARIO WHERE C.ID_CITA=@ID_CITA AND M.ID_USUARIO=@ID_USUARIO;
 SELECT ID_PAGO,MONTO,METODO,ESTADO,CODIGO_TRANSACCION,ULTIMOS_4,FECHA_PAGO FROM dbo.TB_PAGO WHERE ID_CITA=@ID_CITA ORDER BY ID_PAGO DESC;
END;
GO
